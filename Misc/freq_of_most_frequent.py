def maxFrequency(nums, k):
    nums.sort()
    freq_dict = {}

    for i in range(len(nums) - 1, -1, -1):
        k_temp = k
        value = nums[i]
        freq_dict[value] = 1
        j = i - 1
        while j >= 0 and k_temp > 0:
            if nums[j] + k_temp < value:
                break
            else:
                freq_dict[value] += 1
                k_temp = nums[j] + k_temp - value
                j = j - 1

    return max(freq_dict.keys())

